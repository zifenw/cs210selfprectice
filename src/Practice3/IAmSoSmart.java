package Practice3;

public class IAmSoSmart {

	public static void main(String[] args) {
		smart(3);
		System.out.println();
		smart(2);
		System.out.println();
		smart(1);
	}
	public static void smart(int times) {
		for (int i = 1; i <= times; i++) {
			System.out.println("I am so smart.");
		}
	}
}
