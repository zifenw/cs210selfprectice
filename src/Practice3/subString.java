package Practice3;

public class subString {

	public static void main(String[] args) {
		String originalString1 = "Hello, World!";
		String substring1 = originalString1.substring(7, 12);
		System.out.println(substring1); // Output: "World"
		
		String originalString2 = "Hello, World!";
		String substring2 = originalString2.substring(7);
		System.out.println(substring2); // Output: "World!"

	}

}
